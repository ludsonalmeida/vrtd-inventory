/* Helpers para agrupar mantendo a ordem das categorias */
const groupByCategory = (items, categories) =>
  categories
    .slice()
    .sort((a, b) => (a.order ?? 999) - (b.order ?? 999))
    .map(cat => ({
      id: cat.id,
      title: cat.title,
      items: items.filter(it => it.categoryId === cat.id),
    }))
    .filter(group => group.items.length > 0);

